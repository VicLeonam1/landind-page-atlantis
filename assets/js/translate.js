// Função para traduzir os textos da página com base no idioma
function translatePage(language) {
  const elements = document.querySelectorAll("[data-key]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-key");
    if (translations[language][key]) {
      // Para inputs, selects, e outros campos de formulário com value
      if (element.placeholder) {
        element.placeholder = translations[language][key]; // Para inputs e placeholders
      } else if (element.value !== undefined) {
        element.value = translations[language][key]; // Para campos com 'value' (inputs, selects)
      } else {
        element.textContent = translations[language][key]; // Para textos comuns
      }
    }
  });
}

// Definir o idioma padrão
let language = "pt";

// Função para alternar o idioma
function changeLanguage(language) {
    // Armazena o idioma selecionado no localStorage
    localStorage.setItem("language", language);
  
    // Atualiza a bandeira e o idioma exibido no menu
    const idiomaImg = document.getElementById("bandeira");
    const idiomaText = document.querySelector(".idioma p");

    if (language === "pt") {
        idiomaImg.src = "/assets/images/portugues.png"; // Bandeira do Brasil
        idiomaText.textContent = "PT-BR";
    } else if (language === "en") {
        idiomaImg.src = "/assets/images/ingles.png"; // Bandeira dos EUA/Reino Unido
        idiomaText.textContent = "EN-US";
    }

    // Atualiza os textos da página conforme o idioma selecionado
    const elements = document.querySelectorAll("[data-key]");
    elements.forEach((element) => {
        const key = element.getAttribute("data-key");
        if (translations[language][key]) {
            if (element.placeholder) {
                element.placeholder = translations[language][key]; // Para inputs
            } else if (element.value !== undefined) {
                element.value = translations[language][key]; // Para campos de formulário
            } else {
                element.textContent = translations[language][key]; // Para textos
            }
        }
    });

    // Fechar a lista de idiomas
    const lista = document.querySelector(".lista-idioma");
    const seta = document.querySelector(".material-symbols-outlined");
    lista.style.opacity = "0"; // Fecha a lista
    seta.style.transform = "rotate(0deg)"; // Restaura a seta
}

// Função para mostrar/ocultar a lista de idiomas
function listaIdiomas() {
    const lista = document.querySelector(".lista-idioma");
    const seta = document.querySelector(".material-symbols-outlined");

    if (lista.style.opacity === "0") {
        lista.style.opacity = "1"; // Exibe a lista
        seta.style.transform = "rotate(180deg)"; // Rotaciona a seta
    } else {
        lista.style.opacity = "0"; // Esconde a lista
        seta.style.transform = "rotate(0deg)"; // Restaura a seta
    }
}

// Função para carregar o idioma salvo ao carregar a página
window.onload = function() {
    if (localStorage.getItem("language")) {
        language = localStorage.getItem("language");
    }
    changeLanguage(language); // Aplica o idioma armazenado
}
